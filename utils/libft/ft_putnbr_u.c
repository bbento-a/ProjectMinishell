#include "libft.h"

static int	ft_num_len(unsigned	int num)
{
	int	len;

	len = 0;
	while (num != 0)
	{
		len++;
		num = num / 10;
	}
	return (len);
}

static char	*ft_unsigned(unsigned int n)
{
	char	*num;
	int		len;

	len = ft_num_len(n);
	num = (char *)malloc(sizeof(char) * (len + 1));
	if (!num)
		return (0);
	num[len] = '\0';
	while (n != 0)
	{
		num[len - 1] = n % 10 + 48;
		n = n / 10;
		len--;
	}
	return (num);
}

int	ft_putnbr_u(unsigned int n)
{
	int		pr;
	char	*num;

	pr = 0;
	if (n == 0)
		pr += write(1, "0", 1);
	else
	{
		num = ft_unsigned(n);
		pr += ft_putstr(num);
		free(num);
	}
	return (pr);
}
